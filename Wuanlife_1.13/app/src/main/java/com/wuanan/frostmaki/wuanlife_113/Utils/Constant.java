public class Constant {
        public static int displayWidth=0;  //屏幕宽度

        public static int displayHeight=0; //屏幕高度

    public static void setDisplayHeight(int displayHeight) {
        Constant.displayHeight = displayHeight;
    }

    public static void setDisplayWidth(int displayWidth) {
        Constant.displayWidth = displayWidth;
    }

    public static int getDisplayHeight() {
        return displayHeight;
    }

    public static int getDisplayWidth() {
        return displayWidth;
    }
}
